package basicprogramgtm;

public class Mathclassjava {

	public static void main(String[] args) 
	{
		
		System.out.println(Math.addExact(100, 200));
        System.out.println(Math.subtractExact(200,20));
        System.out.println(Math.multiplyExact(0, 0));
        System.out.println(Math.max(90, 80)); // will print maximum value
        System.out.println(Math.min(3.5, 2.5)); // will print minimum value
        System.out.println(Math.abs(-800)); // absolute value will give positive value 
        System.out.println(Math.random()); // it will be a random value everytime, the value will be 0 and 1 always 
        // the value will be always double data type for math.random
        
	}

}
